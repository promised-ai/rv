fn unit_gauss_legendre_quadrature_cached<F>(
    f: F,
    weights: &[f64],
    roots: &[f64],
) -> f64
where
    F: Fn(f64) -> f64,
{
    weights
        .iter()
        .zip(roots.iter())
        .map(|(weight, &root)| weight * f(root))
        .sum::<f64>()
}

