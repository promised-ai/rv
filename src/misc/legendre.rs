///     * `f`: Numerical function (`Fn(f64) -> f64`)
///     * `(a,b)`: Interval of integration
///     * `weights` the vector of weights
///     * `roots` the vector of roots
///
/// # Reference
/// * A. N. Lowan et al. (1942)
